CREATE PROCEDURE ERROR_04(@ENO INT, @ENAME VARCHAR(15), @SAL INT)
AS
BEGIN
	BEGIN TRY
		INSERT INTO EMP (EMPNO, ENAME, SAL) VALUES (@ENO, @ENAME, @SAL)
	END TRY
	BEGIN CATCH
