CREATE PROCEDURE LOOPS_01
AS
BEGIN
	DECLARE @X INT = 1
	DECLARE @Y INT = 10
	WHILE @X <= @Y
	BEGIN
		PRINT @X
		SET @X = @X +1
	END
END


EXEC LOOPS_01



CREATE PROCEDURE LOOPS_02
AS
BEGIN
	DECLARE @X INT = 1
	DECLARE @Y INT = 10
	WHILE @X <= @Y
	BEGIN	
		PRINT @X
		SET @X = @X +1
		IF @X = 5
			BREAK
	END
END

EXEC LOOPS_02



CREATE PROCEDURE LOOP_3(@X INT, @Y INT)
AS
BEGIN
	WHILE @X  <= @Y
	BEGIN
		IF @X %2 = 0
			PRINT @X
		SET @X = @X + 1
	END
END

