-- GET ORDER IDS MATCHED BETWEEN BOTH TABLES [ORDER ID], [CUSTOMER ID], SALES, PROFIT
SELECT SO.[Order ID], SO.[Customer ID], SO.Sales, SO.Profit FROM SALES_DATA SO INNER JOIN Returns RT ON SO.[Order ID] = RT.[Order ID]

-- GET ALL ORDER ID'S ONLY FROM SALES DATA EVEN IF THERE IS NO MATCH WITH RETURNS [ORDER ID], [CUSTOMER ID], SALES, PROFIT, FROM RETURNS TABLE -- RETURNED, ORDER ID
SELECT SO.[Order ID], SO.[Customer ID], SO.Sales, SO.Profit, RT.[Order ID], RT.Returned FROM SALES_DATA SO LEFT JOIN Returns RT ON SO.[Order ID] = RT.[Order ID]



