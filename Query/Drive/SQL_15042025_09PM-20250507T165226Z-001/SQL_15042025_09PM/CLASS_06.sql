-- JOINS
-- INNER JOIN
SELECT * FROM EMP INNER JOIN DEPT ON EMP.DEPTNO = DEPT.DEPTNO  
SELECT A.ENAME, A.EMPNO, A.SAL, B.DNAME, B.LOC FROM EMP A INNER JOIN DEPT B ON A.DEPTNO = B.DEPTNO

-- LEFT JOIN
SELECT * FROM EMP LEFT JOIN DEPT ON EMP.DEPTNO = DEPT.DEPTNO
SELECT A.ENAME, A.EMPNO, A.SAL, B.DNAME, B.LOC FROM EMP A LEFT JOIN DEPT B ON A.DEPTNO = B.DEPTNO

-- RIGHT JOIN
SELECT * FROM EMP RIGHT JOIN DEPT ON EMP.DEPTNO = DEPT.DEPTNO
SELECT A.ENAME, A.EMPNO, A.SAL, B.DNAME, B.LOC FROM EMP A RIGHT JOIN DEPT B ON A.DEPTNO = B.DEPTNO

-- FULL JOIN
SELECT * FROM EMP FULL JOIN DEPT ON EMP.DEPTNO = DEPT.DEPTNO
SELECT A.ENAME, A.EMPNO, A.SAL, B.DNAME, B.LOC FROM EMP A FULL JOIN DEPT B ON A.DEPTNO = B.DEPTNO

