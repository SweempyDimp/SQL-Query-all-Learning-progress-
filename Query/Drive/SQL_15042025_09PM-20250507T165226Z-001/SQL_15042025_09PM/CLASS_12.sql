CREATE PROCEDURE LOOPS_01
AS
BEGIN
	DECLARE @X INT = 1
	DECLARE @Y INT = 10
	WHILE @X <= @Y
	BEGIN
		PRINT @X
		SET @X = @X + 1
	END
END

