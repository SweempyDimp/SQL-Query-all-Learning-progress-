CREATE PROCEDURE ER_01(@X INT, @Y INT)
AS
BEGIN
	BEGIN TRY
		PRINT @X /@Y
	END TRY
	BEGIN CATCH
		PRINT 'ZERO ERROR DIVISION PLEASE CHECK INPUTS'
	END CATCH
END

