SELECT a.customerid, a.CustomerID, a.City, b.OrderID, b.OrderDate ,
	   c.Quantity, d.ProductName, d.Price, d.Unit, e.CategoryName, f.SupplierName
FROM customers a 
left JOIN orders b			ON a.customerid = b.customerid
left JOIN OrderDetails c	on c.OrderID = b.OrderID
left join Products d		on d.ProductID = c.ProductID
LEFT JOIN Categories e		ON e.CategoryID = d.CategoryIDLEFT JOIN Suppliers f		ON f.SupplierID = d.SupplierID

