SELECT * FROM EMP
-- 1980-12-17 -- YEAR, MONTH, DAT, QUATER, WEEKDAY, WEEKNUM, DATEDIFF
-- DATEPART(INTERVAL, DATE_COLUMN)
SELECT HIREDATE,
DATEPART(YEAR, HIREDATE) AS YR,
DATEPART(MONTH, HIREDATE) AS MON,
DATEPART(QUARTER, HIREDATE) AS QR,
DATEPART(WEEK, HIREDATE) AS WK
FROM EMP

SELECT HIREDATE, 
DATEPART(YEAR, HIREDATE) AS YR,
DATENAME(MONTH, HIREDATE)AS MN,         --NAME
DATEPART(DAY, HIREDATE) AS DY,
DATENAME(QUARTER, HIREDATE) AS QR,
DATENAME(WEEKDAY, HIREDATE) AS WK
FROM EMP

-- DATEADD
SELECT HIREDATE,
DATEADD(YEAR, 1, HIREDATE),
DATEADD(MONTH, 1, HIREDATE) 
FROM EMP

-- DATEDIFF
SELECT [ORDER DATE], [SHIP DATE],
DATEDIFF(DAY, [ORDER DATE], [SHIP DATE]) AS DAYS_DIFF
FROM SALES_DATA

SELECT HIREDATE,
DATEDIFF(YEAR, HIREDATE, GETDATE()) AS YEARS_DIFF,
DATEDIFF(MONTH, HIREDATE, GETDATE()) AS MONTH_DIFF
FROM EMP

